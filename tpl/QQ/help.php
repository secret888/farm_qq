<div class="faqa">升级才能解锁建筑，勇士，地图等，才能采集更多的粮食。</div>
<div class="faqq">做任务有什么好处？</div>
<div class="faqa">做任务可以加快升级的速度，并且解锁相关建筑，地图。</div>
<div class="faqq">粮食的作用？</div>
<div class="faqa">治疗受伤的士兵；修复受损的建筑。</div>
<div class="faqq">原石的作用？</div>
<div class="faqa">游戏币，可购买游戏中的建筑，勇士，地图等，可用粮食在游戏中的村庄中购买，游戏中击败敌军也可获得，也可用精石直接购买。</div>
<div class="faqq">精石的作用？</div>
<div class="faqa">通过人人豆兑换获得。可购买游戏中的高级道具，高级勇士，直接完成任务等。</div>
