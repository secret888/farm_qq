<div class="faqa">游戏中大部分操作都要消耗能量，每5分钟自动恢复1点，上限为30点，也可在商店中购买能量包立即补充能量。</div>
<div class="faqq">什么是愿望清单？</div>
<div class="faqa">打开背包，可以将你想要的物品添加到愿望清单，好友就可以再他的好友列表中看到并且赠送给你。</div>
<div class="faqq">收集有什么用？</div>
<div class="faqa">通过消灭萨魔族来收集战利品。收集到的战利品可以通过背包里的物品兑换来换取您所需要的物品。</div>
<div class="faqq">怎样移动，出售建筑？</div>
<div class="faqa">点击右下角手型图标，在弹出的操作列表中选择移动或者出售。</div>
<div class="faqq">重新部署勇士？</div>
<div class="faqa">点击右下角手型图标，在弹出的操作列表中选择重新部署，即可把勇士放回物品栏中，以便重新部署。</div>
